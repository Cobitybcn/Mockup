            function generateOneMockup(artworkId, contextId, btn) {
                if (!confirm('Are you sure you want to generate one mockup from this context using the sovereign Admin V7 prompt?')) {
                    return;
                }

                // Validate IDs before sending
                if (!artworkId || !contextId || artworkId <= 0 || contextId <= 0) {
                    alert('Error: artwork_id or context_id is missing. Cannot generate.');
                    console.error('generateOneMockup called with invalid IDs:', { artworkId, contextId });
                    return;
                }

                const statusDiv = document.getElementById('gen-status-' + contextId);
                btn.disabled = true;
                btn.style.opacity = '0.7';
                const originalBtnText = btn.textContent;
                btn.textContent = 'Generating...';
                statusDiv.innerHTML = '<span style="color: var(--muted); font-weight: bold;">[Vertex Bridge calling...] Please wait.</span>';

                const formData = new FormData();
                formData.append('artwork_id', String(artworkId));
                formData.append('context_id', String(contextId));

                console.log('[generateOneMockup] POST artwork_id=' + artworkId + ' context_id=' + contextId);

                fetch('generate_one_mockup_from_composed_admin_prompt.php', {
                    method: 'POST',
                    body: formData
                })
                .then(function(response) {
                    const httpStatus = response.status;
                    return response.text().then(function(rawText) {
                        console.log('[generateOneMockup] HTTP ' + httpStatus + ' raw response:', rawText);
                        let parsed = null;
                        try {
                            parsed = JSON.parse(rawText);
                        } catch (parseErr) {
                            console.error('[generateOneMockup] JSON parse failed. Raw response was:', rawText);
                            throw new Error('Server returned a non-JSON response (HTTP ' + httpStatus + '). Check PHP logs. Preview: ' + rawText.substring(0, 300));
                        }
                        return { status: httpStatus, body: parsed };
                    });
                })
                .then(function(res) {
                    if (res.status === 200 && res.body.ok) {
                        const auditParts = res.body.audit_file ? res.body.audit_file.split('/') : [];
                        const auditFilename = auditParts[auditParts.length - 1] || '';
                        statusDiv.innerHTML = '<span style="color: #234E52; font-weight: bold;">✓ Success! Mockup generated.</span> ' +
