                    btn.disabled = false;
                    btn.style.opacity = '1';
                    btn.textContent = originalBtnText;
                    console.error('[generateOneMockup] Caught error:', err);
                });
            }

            async function generate6Mockups(artworkId, btn) {
                const contextIds = JSON.parse(btn.getAttribute('data-context-ids') || '[]');
                if (contextIds.length === 0) {
                    alert('No context proposals available to generate.');
                    return;
                }

                if (!confirm(`Are you sure you want to generate mockups for the first ${contextIds.length} reviewed context proposals?`)) {
                    return;
                }

                btn.disabled = true;
                btn.style.opacity = '0.7';
                const originalText = btn.textContent;
                btn.textContent = 'Generating Batch...';

                const progressPanel = document.getElementById('gen-6-progress-panel');
                const statusDiv = document.getElementById('gen-6-status');
                const logsDiv = document.getElementById('gen-6-logs');

                progressPanel.style.display = 'block';
                statusDiv.innerHTML = '<span style="color: var(--muted);">Starting generation round...</span>';
                logsDiv.innerHTML = '';

                let successCount = 0;
                let failCount = 0;

                for (let i = 0; i < contextIds.length; i++) {
                    const contextId = contextIds[i];
                    const stepNum = i + 1;
                    statusDiv.innerHTML = `Generating proposal ${stepNum} of ${contextIds.length}...`;

                    // Create log line placeholder
                    const logLine = document.createElement('div');
                    logLine.style.padding = '8px 12px';
                    logLine.style.background = 'var(--surface-soft)';
                    logLine.style.border = '1px solid var(--line)';
                    logLine.style.borderRadius = '4px';
                    logLine.innerHTML = `<strong>Proposal ID #${contextId}:</strong> <span style="color: var(--muted);">Calling Vertex Bridge...</span>`;
                    logsDiv.appendChild(logLine);

                    // Update individual button/status div if they exist on page
                    const indStatusDiv = document.getElementById('gen-status-' + contextId);
                    if (indStatusDiv) {
                        indStatusDiv.innerHTML = '<span style="color: var(--muted); font-weight: bold;">[Vertex Bridge calling...] Please wait.</span>';
                    }

                    try {
                        const formData = new FormData();
                        formData.append('artwork_id', String(artworkId));
                        formData.append('context_id', String(contextId));

                        const response = await fetch('generate_one_mockup_from_composed_admin_prompt.php', {
                            method: 'POST',
                            body: formData
                        });

                        const httpStatus = response.status;
                        const rawText = await response.text();
                        console.log(`[generate6Mockups] Proposal ${contextId} HTTP ${httpStatus} raw:`, rawText);

                        let parsed = null;
                        try {
                            parsed = JSON.parse(rawText);
                        } catch (parseErr) {
                            throw new Error(`Server returned a non-JSON response (HTTP ${httpStatus}).`);
                        }

                        if (httpStatus === 200 && parsed.ok) {
                            successCount++;
                            const logHtml = `<strong>Proposal ID #${contextId}:</strong> <span style="color: #234E52; font-weight: bold;">✓ Success! Mockup #${parsed.mockup_id} generated.</span> ` +
                                `<a href="view_mockup_file.php?mockup_id=${encodeURIComponent(parsed.mockup_id)}" target="_blank" style="margin-left: 8px; font-weight: bold; text-decoration: underline; color: #1a73e8;">View Mockup</a> | ` +
                                `<a href="view_mockup_generation_audit.php?id=${encodeURIComponent(parsed.mockup_id)}" target="_blank" style="font-weight: bold; text-decoration: underline; color: #1a73e8;">View Audit JSON</a>`;
                            logLine.innerHTML = logHtml;

                            if (indStatusDiv) {
                                indStatusDiv.innerHTML = logHtml;
                            }
                        } else {
                            failCount++;
                            const errMsg = (parsed && parsed.error) ? parsed.error : `HTTP ${httpStatus}`;
                            const logHtml = `<strong>Proposal ID #${contextId}:</strong> <span style="color: var(--danger); font-weight: bold;">✗ Failed: ${errMsg}</span>`;
                            logLine.innerHTML = logHtml;

                            if (indStatusDiv) {
                                indStatusDiv.innerHTML = logHtml;
                            }
                        }
                    } catch (err) {
                        failCount++;
                        const errMsg = err && err.message ? err.message : String(err);
                        const logHtml = `<strong>Proposal ID #${contextId}:</strong> <span style="color: var(--danger); font-weight: bold;">✗ Failed: ${errMsg}</span>`;
                        logLine.innerHTML = logHtml;

                        if (indStatusDiv) {
                            indStatusDiv.innerHTML = logHtml;
                        }
                    }
                }

                statusDiv.innerHTML = `<span style="color: #234E52; font-weight: bold;">✓ Completed!</span> ${successCount} generated successfully, ${failCount} failed.`;
                btn.textContent = originalText;
                btn.disabled = false;
                btn.style.opacity = '1';
            }

