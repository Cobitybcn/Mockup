                        statusDiv.innerHTML = '<span style="color: var(--danger); font-weight: bold;">Error: ' + errMsg + '</span>';
                        console.error('[generateOneMockup] Endpoint error response:', res.body);
                        btn.disabled = false;
                        btn.style.opacity = '1';
                        btn.textContent = originalBtnText;
                    }
                })
                .catch(function(err) {
